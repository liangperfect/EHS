package com.example.ehs.model;

public class Music {
	int musicRating;
	String musicPath;
	String musicName;
	String musicAlbum;
	String musicArtist;
	String musicPicPath;
	public int getMusicRating() {
		return musicRating;
	}
	public void setMusicRating(int musicRating) {
		this.musicRating = musicRating;
	}
	public String getMusicPath() {
		return musicPath;
	}
	public void setMusicPath(String musicPath) {
		this.musicPath = musicPath;
	}
	public String getMusicName() {
		return musicName;
	}
	public void setMusicName(String musicName) {
		this.musicName = musicName;
	}
	public String getMusicAlbum() {
		return musicAlbum;
	}
	public void setMusicAlbum(String musicAlbum) {
		this.musicAlbum = musicAlbum;
	}
	public String getMusicArtist() {
		return musicArtist;
	}
	public void setMusicArtist(String musicArtist) {
		this.musicArtist = musicArtist;
	}
	public String getMusicPicPath() {
		return musicPicPath;
	}
	public void setMusicPicPath(String musicPicPath) {
		this.musicPicPath = musicPicPath;
	}
	
}
